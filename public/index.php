            $(document).on("click", ".addtoCart", function (e) {
                e.preventDefault();
                
                const productId = String($(this).data("id"));
                const cart = addToCartId(productId, { unique: true });
                Swal.fire({
                    icon: "success",
                    title: "Added to cart successfully",
                    text: "Added",
                    timer: 1500,
                    showConfirmButton: false
                });
                console.log("Cart updated:", cart);
            });

            function loadProducts(data = {}) {
                console.log("sss",data);
                $.ajax({
                    url: "/tanore/controller/get-all-product-index.php",
                    type: "GET",         
                    dataType: "json",
                    data: data,
                    success: function (response) {

                        if (response.value === true) {

                            const products = response.data;
                            console.log("products :- ", products);


                            const container = document.getElementById("productsList");
                            container.innerHTML = "";

                            products.forEach(product => {
                                // Convert comma-separated images to array
                                let imagesLink = "";
                                if (product.images) {
                                    imagesLink = `${window.location.origin}/tanore/uploads/${product.images}`;
                                } else {
                                    imagesLink = `${window.location.origin}/tanore/public/assets/img/no-image.png`;
                                }

                                // Product UI block
                                const cardHTML = `
                                        <div class="col-md-3 col-sm-6 col-xs-12 product_itm">
                                            <figure class="itg-hover-style2">
                                                <img src="${imagesLink}" alt="">
                                                <div class="products-boxes">
                                                    <a id="shopClick" class="itg-button">View More</a>
                                                </div>
                                                <figcaption class=" figure-boxx figure-section-hover text-center">
                                                    <h4 class="theme-headdings text-center pad-top10">
                                                        <a>${product.product_name}</a></h4>
                                                    <p>₹${Number(product.product_price).toLocaleString("en-IN")}</p>
                                                    <a id="cartClick" data-id="${product.product_id}" class="addtoCart itg-button">Add to Cart</a>
                                                </figcaption>
                                            </figure>
                                        </div>
                                    `;

                                container.innerHTML += cardHTML;
                            });
                            return;
                        }

                    },
                    error: function (xhr) {
                        console.error(xhr.responseText);
                        Swal.fire("Error", "Server error", "error");
                    }
                });
            }

            const data = {
                limit: 8
            };

            loadProducts(data);
        </script>
